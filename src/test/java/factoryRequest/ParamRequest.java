package factoryRequest;

public class ParamRequest {

    private String Url;
    private String Body;


    public ParamRequest() {}

    public String getUrl() {
        return Url;
    }

    public void setUrl(String vStrUrl) {
        this.Url = vStrUrl;
    }

    public String getBody() {
        return Body;
    }

    public void setBody(String vStrBody) {
        this.Body = vStrBody;
    }
}
